# age category

age = eval(input("Please input your age: "))

if age <= 7:
	print("=======")
	print("Toddler")

if age >=8 and age <=13:
	print("=======")
	print("Pre teen")

if age >=14 and age <=18:
	print("=======")
	print("Teenager")

if age >=19 and age <=31:
	print("=======")
	print("Early adulthood")

if age >=32 and age <=45:
	print("=======")
	print("Mid adulthood")

if age >=46 and age <=59:
	print("=======")
	print("Post adulthood")

if age >=60:
	print("=======")
	print("Senior")

